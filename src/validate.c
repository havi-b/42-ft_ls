#include "../includes/ft_ls.h"

int			is_ls_flag(char c)
{
	return (c == 'a' || c == 'l' || c == 'r' || c == 'R' || c == 't');
}

int			is_ls_hidden(char *s)
{
	return ((s[0] == '.' && s[1] == '\0') || \
	(s[0] == '.' && s[1] == '.' && s[2] == '\0'));
}
